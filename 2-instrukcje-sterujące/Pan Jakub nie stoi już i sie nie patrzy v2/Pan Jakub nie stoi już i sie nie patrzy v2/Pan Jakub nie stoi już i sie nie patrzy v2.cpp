
#include <iostream>
using namespace std;

int main()
{


	// Całkowite- int, short, long, unsigned short
	short a, b;

	cout << "Podaj a: ";
	cin >> a;
	cout << "Podaj b: ";
	cin >> b;


	// Instrukcja warunkowa - if(warunek)
	if (a < b) {
		cout << a << " < " << b;
	}
	else if (a == b) {
		cout << a << " == " << b;
	}



	else {
		cout << a << " > " << b;
	}









}
